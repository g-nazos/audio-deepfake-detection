def paragraphs_from_text(text, words_per_paragraph=13):
    words = text.split()
    result = []
    for i in range(0, len(words), words_per_paragraph):
        chunk = words[i : i + words_per_paragraph]
        result.append(" ".join(chunk))
    return result


